for numero in range(1, 10):
    #A linha abaixo é executada 10 vezes
    print("Vou mostrar a tabuado do", numero)

    for multiplicador in range(1, 11):
        #A linha abaixo é executada 100 vezes
        print(numero, 'x', multiplicador, '=', numero * multiplicador)

    print("\n----------------------------\n")
